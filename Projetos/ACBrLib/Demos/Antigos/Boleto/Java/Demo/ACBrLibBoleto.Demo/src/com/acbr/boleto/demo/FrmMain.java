    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        try {
            PrintService[] printServices = PrintServiceLookup.lookupPrintServices(null, null);
            for (PrintService printer : printServices){
                cmbImpressora.addItem(printer.getName());
            }
            
            if(printServices.length > 0)
                cmbImpressora.setSelectedIndex(0);
                                   
            // Altera as config de log
            Path pathLog = Paths.get(System.getProperty("user.dir"), "Log");
            if (!Files.isDirectory(pathLog)) {
                pathLog.toFile().mkdirs();
            }
            
            int ret = acbrBoleto.Boleto_ConfigGravarValor(ACBrBoleto.toUTF8("Principal"), ACBrBoleto.toUTF8("LogNivel"), "4");
            ACBrBoleto.checkResult(ret);
            
            ret = acbrBoleto.Boleto_ConfigGravarValor(ACBrBoleto.toUTF8("Principal"), ACBrBoleto.toUTF8("LogPath"), pathLog.toString());
            ACBrBoleto.checkResult(ret);
            
            ret = acbrBoleto.Boleto_ConfigGravar(ACBrBoleto.toUTF8(""));
            ACBrBoleto.checkResult(ret);
            
            loadConfig();
            
        } catch (Exception ex) {
            Logger.getLogger(FrmMain.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_formWindowActivated

    private void btnIncluirTituloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncluirTituloActionPerformed
                  
        try{
            JFileChooser chooser = new JFileChooser();
            OpenFileFilter filter = new OpenFileFilter ("ini", "Titulo (*.ini)");
            chooser.addChoosableFileFilter(filter);
            chooser.setFileFilter(filter);
            int returnVal = chooser.showSaveDialog(rootPane);
            if (returnVal != JFileChooser.APPROVE_OPTION) return;
            
            int ret;
            ByteBuffer buffer = ByteBuffer.allocate(256);
            IntByReference bufferLen = new IntByReference(256);
            
            ret = acbrBoleto.Boleto_IncluirTitulos(ACBrBoleto.toUTF8(chooser.getSelectedFile().getPath()), 
                    ACBrBoleto.toUTF8(""), 
                    buffer, 
                    bufferLen);
            ACBrBoleto.checkResult(ret);
            
            if(bufferLen.getValue() > 256){
                buffer = ByteBuffer.allocate(bufferLen.getValue());                        
                ret = acbrBoleto.Boleto_UltimoRetorno(buffer, bufferLen);
                ACBrBoleto.checkResult(ret);
            }
                        
             rtbRespostas.append( ACBrBoleto.fromUTF8(buffer, bufferLen.getValue())+"\n");
                        
            
        } catch (Exception ex) {
            Logger.getLogger(FrmMain.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnIncluirTituloActionPerformed

