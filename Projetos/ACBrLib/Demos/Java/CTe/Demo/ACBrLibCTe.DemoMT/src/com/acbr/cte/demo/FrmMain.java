    private void loadConfig() {
        try {
            acbrCTe.configLer();
            cmbModeloDocumento.setSelectedIndex(Integer.parseInt(acbrCTe.configLerValor(ACBrSessao.CTe, "ModeloDF")));
            cmbCrypt.setSelectedIndex(Integer.parseInt(acbrCTe.configLerValor(ACBrSessao.DFe, "SSLCryptLib")));            
            cmbHttp.setSelectedIndex(Integer.parseInt(acbrCTe.configLerValor(ACBrSessao.DFe, "SSLHttpLib")));    
            cmbXmlSign.setSelectedIndex(Integer.parseInt(acbrCTe.configLerValor(ACBrSessao.DFe, "SSLXmlSignLib")));
            txtCertPath.setText(acbrCTe.configLerValor(ACBrSessao.DFe, "ArquivoPFX"));
            txtCertPassword.setText(acbrCTe.configLerValor(ACBrSessao.DFe, "Senha"));
            txtCertNumero.setText(acbrCTe.configLerValor(ACBrSessao.DFe, "NumeroSerie"));
            txtSchemaPath.setText(acbrCTe.configLerValor(ACBrSessao.CTe, "PathSchemas"));
            cmbUfDestino.setSelectedItem(acbrCTe.configLerValor(ACBrSessao.DFe, "UF"));
            
            String ambiente = acbrCTe.configLerValor(ACBrSessao.CTe, "Ambiente");            
            rdbHomologacao.setSelected("1".equals(ambiente));
            rdbProducao.setSelected("0".equals(ambiente));
                        
            cmbSSlType.setSelectedIndex(Integer.parseInt(acbrCTe.configLerValor(ACBrSessao.CTe, "SSLType"))); 
            nudTimeOut.setValue(Integer.parseInt(acbrCTe.configLerValor(ACBrSessao.CTe, "Timeout")));
            txtProxyServidor.setText(acbrCTe.configLerValor(ACBrSessao.Proxy, "Servidor"));
            String Value = acbrCTe.configLerValor(ACBrSessao.Proxy, "Porta");
            if(Value.matches("-?\\d+"))
                nudProxyPorta.setValue(Integer.parseInt(Value));
            txtProxyUsuario.setText(acbrCTe.configLerValor(ACBrSessao.Proxy, "Usuario"));
            txtProxySenha.setText(acbrCTe.configLerValor(ACBrSessao.Proxy, "Senha"));
            txtNome.setText(acbrCTe.configLerValor(ACBrSessao.Email, "Nome"));
            txtEmail.setText(acbrCTe.configLerValor(ACBrSessao.Email, "Conta"));
            txtUsuario.setText(acbrCTe.configLerValor(ACBrSessao.Email, "Usuario"));
            txtSenha.setText(acbrCTe.configLerValor(ACBrSessao.Email, "Senha"));
            txtHost.setText(acbrCTe.configLerValor(ACBrSessao.Email, "Servidor"));
            Value = acbrCTe.configLerValor(ACBrSessao.Email, "Porta");
            if(Value.matches("-?\\d+"))
                nudPorta.setValue(Integer.parseInt(Value));          
            ckbSSL.setSelected(acbrCTe.configLerValor(ACBrSessao.Email, "SSL").equals("1"));            
            ckbTLS.setSelected(acbrCTe.configLerValor(ACBrSessao.Email, "TLS").equals("1"));

        } catch (Exception ex) {
            Logger.getLogger(FrmMain.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

